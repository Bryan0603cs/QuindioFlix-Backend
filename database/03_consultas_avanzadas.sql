-- 3.1.5 Fragmentación física por rango de fechas con tablespaces.
-- Ejecutar este bloque con un usuario DBA o con privilegio CREATE TABLESPACE.
-- Si tu Oracle no permite rutas relativas para DATAFILE, cambia los nombres por rutas absolutas válidas.
CREATE TABLESPACE TS_REP_2024 DATAFILE 'ts_rep_2024.dbf' SIZE 100M AUTOEXTEND ON NEXT 20M MAXSIZE 500M;
CREATE TABLESPACE TS_REP_2025 DATAFILE 'ts_rep_2025.dbf' SIZE 100M AUTOEXTEND ON NEXT 20M MAXSIZE 500M;
CREATE TABLESPACE TS_REP_FUTURO DATAFILE 'ts_rep_futuro.dbf' SIZE 100M AUTOEXTEND ON NEXT 20M MAXSIZE 500M;

-- Tabla fragmentada para evidenciar la estrategia sin destruir la tabla operacional REPRODUCCIONES.
-- Se usa el mismo diseño lógico y se particiona por FECHA_HORA_INICIO.
BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE REPRODUCCIONES_FRAGMENTADA PURGE';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE != -942 THEN RAISE; END IF;
END;
/

CREATE TABLE REPRODUCCIONES_FRAGMENTADA (
  ID_REPRODUCCION NUMBER(19,0),
  ID_PERFIL NUMBER(19,0) NOT NULL,
  ID_CONTENIDO NUMBER(19,0) NOT NULL,
  ID_EPISODIO NUMBER(19,0),
  FECHA_HORA_INICIO TIMESTAMP NOT NULL,
  FECHA_HORA_FIN TIMESTAMP,
  DISPOSITIVO VARCHAR2(20) NOT NULL,
  PORCENTAJE_AVANCE NUMBER(3) NOT NULL
)
PARTITION BY RANGE (FECHA_HORA_INICIO) (
  PARTITION REP_2024 VALUES LESS THAN (TIMESTAMP '2025-01-01 00:00:00') TABLESPACE TS_REP_2024,
  PARTITION REP_2025 VALUES LESS THAN (TIMESTAMP '2026-01-01 00:00:00') TABLESPACE TS_REP_2025,
  PARTITION REP_FUTURO VALUES LESS THAN (MAXVALUE) TABLESPACE TS_REP_FUTURO
);

INSERT INTO REPRODUCCIONES_FRAGMENTADA (
  ID_REPRODUCCION, ID_PERFIL, ID_CONTENIDO, ID_EPISODIO, FECHA_HORA_INICIO,
  FECHA_HORA_FIN, DISPOSITIVO, PORCENTAJE_AVANCE
)
SELECT ID_REPRODUCCION, ID_PERFIL, ID_CONTENIDO, ID_EPISODIO, FECHA_HORA_INICIO,
       FECHA_HORA_FIN, DISPOSITIVO, PORCENTAJE_AVANCE
FROM REPRODUCCIONES;

COMMIT;

SELECT TABLE_NAME, PARTITION_NAME, TABLESPACE_NAME, HIGH_VALUE
FROM USER_TAB_PARTITIONS
WHERE TABLE_NAME = 'REPRODUCCIONES_FRAGMENTADA'
ORDER BY PARTITION_POSITION;
